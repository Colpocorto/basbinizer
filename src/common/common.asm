macro CHANGE_BORDER color
	push	bc
	ld	a,(#0006)
	inc	a
	ld	c,a
	ld	a,color
	di
	out	(c),a
	and	#c0
	out	(#aa),a	
	ld	a,87h	;register 7 + 80h
	out	(c),a
	pop	bc
endmacro
macro CHANGE_BORDER_A
	push	bc
	push	af
	ld	a,(#0006)
	inc	a
	ld	c,a
	pop	af
	di
	out	(c),a
	and	#80
	out	(#aa),a	
	ld	a,87h	;register 7 + 80h
	out	(c),a
	pop	bc
endmacro

